public class TestConstant {
    public static void main(String[] args) {
        System.out.println(100);
        System.out.println(11.5D);
        System.out.println(2.5F);
        System.out.println('a');
        System.out.println(true);
        System.out.println(false);
        System.out.println("false");
    }
}
