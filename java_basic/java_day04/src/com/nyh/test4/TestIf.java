package com.nyh.test4;

/**
 * @author 28304
 * @date 2025/6/4
 * @description 基本If结构：对已知条件进行判断 根据结构决定是否执行if结构以内的代码块
 */
public class TestIf {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a < b) {
            System.out.println("a小于b条件成立");
        }

        System.out.println("程序结束");
    }
}
