package com.nyh.test4;

/**
 * @author 28304
 * @date 2025/5/31
 * @description 关系运算符：> < >= <= == != 关系运算符的结果为布尔值 表示两个操作符或者两个表达式之间的关系是否成立
 * <p>
 * Ctrl + D：复制光标所在行
 */
public class TestRelationOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
